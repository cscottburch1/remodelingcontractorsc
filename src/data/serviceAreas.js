/**
 * Helper function to get service area by slug
 */
export function getServiceAreaBySlug(slug) {
  return serviceAreas.find(area => area.slug === slug);
}

/**
 * Helper function to get all service area paths for routing
 */
export function getAllServiceAreaPaths() {
  return serviceAreas.map(area => area.servicePath);
}

/**
 * Helper function to get nearby towns for internal linking
 */
export function getNearbyTowns(currentSlug) {
  const currentArea = getServiceAreaBySlug(currentSlug);
  if (!currentArea || !currentArea.nearbyTowns) return [];
  
  return currentArea.nearbyTowns.map(townName => {
    return serviceAreas.find(area => area.name === townName);
  }).filter(Boolean);
}
